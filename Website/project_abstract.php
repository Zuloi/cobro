			<h2>Aufgabenstellung</h2>
			<p>
			Wir haben im Basislehrjahr eine programmierbare Drohne. Die Drohne stand nur eingpackt da, ich habe mir gedacht dass, das ein tolles Projekt geben würde wenn ich sie programmieren kann. Meine Ziele waren das ich die Drohne programmieren kann und das auch andere Lehrnende die Drohne während der Pausenzeiten programmieren und fliegen können. Dafür hatte ich vom 28.11.2016 bis am 09.01.2017 Zeit. Die anderen Lehrnende, die im Basislehrjahr sind haben andere tolle Projekte gemacht.
			</p>
			<h2>Ergebnisse</h2>
			<p>
			Ich kann die Drohne mit Scratch programmieren ohne hilfen. Die Drohne kann man leicht mit scratch programmieren. Mit hilfe meiner Webseite, die Erklärungen hat wie man die Drohne mit Scratch programmieren kann. Auf meiner Webseite hat es noch 4 eifache Beispiele.
			</p>
			<h2>Lösungskonzept</h2>
			<p>
			Ich musste am Anfang Scratch herunterladen und Rokit in Scatch installieren, als das nicht funktioniert hat bekamm ich eine Exe, die ich öffnen konnte und dann musste ich mich informieren. Ich fing an, die Befehlsblöcke auszuprobieren und nach einiger zeit fing ich an Programme zu schreiben. Nach einigen Programmen habe ich meine Webseite gemacht. Nach der Webseite hab ich noch weiter Programmiert.
			</p>
			<h2>Aufgetretene Probleme</h2>
			<p>
			Ich habe am Anfang viel Zeit verlohren da ich zuerst versuchte Scatch mit Rokit auszustatten und das nicht gut funktioniert hat. Man musste viel ausprobieren, da die blöcke keine einheiten hatte, man konnte nur Werte einfügen, die man immer wieder testet und hofft das es passt und sonst der Wert angepasst worde bis es funktionierte. Zudem fliegt die Drohne nicht zuverlässlich. Die Drohne fliegt nicht gerade und kann nicht in der Luft am selben Ort fliegen. Die Drohne fliegt auch nicht immer gleich bei schwirigeren Programmen. Der Akku kommt dazu, er hält rund 15 Minuten und man muss ihn 40 Minuten laden.

			</p>
			<h2>Ausblick</h2>
			<p>
		Für mich ist dieses Projekt abgeschlossen, ich hoffe es wird benutzt und ich habe gelernt, wie man mit den Befehlen die Drohne programmiert.
			</p>
